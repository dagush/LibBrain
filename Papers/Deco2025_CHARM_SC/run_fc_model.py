"""
Deco2025_CHARM_SC/examples/run_fc_model.py
----------------------------------
Reproduces FCmodel.m from Deco et al. (2025).

Compares FC generated by random-walk simulations (HARM and CHARM-SC)
against empirical FC computed from real BOLD data.

Original MATLAB               →   This file
------------------------------    --------------------------------
load schaefercog.mat          →   DL.get_parcellation().get_CoGs()
load hcp_REST_LR_schaefer1000 →   DL (HCP DataLoader)
filtfilt + corrcoef           →   BandPassFilter + NeuroNumba FC observable
%% HARM SC + %% CHARM SC       →   HARM, CHARM_SC (geometry models)
inner simulation loop         →   BOLDGenerator.simulate_fc()
corrcoef(FCemp, FCsim)        →   scipy.stats.pearsonr on lower triangles
boxplot + ranksum              →   matplotlib violinplot + scipy.stats.ranksums

Demo parameters (reduced from paper for speed):
    NSUB      = 1003  → n_subjects = 10
    NTRIALS   = 1000  → n_trials   = 20
    NREP      = 200   → n_reps     = 10
    T_sim     = 1000  → n_timesteps = 200
    N         = 1000  (unchanged — determined by parcellation)

Usage
-----
    python examples/run_fc_model.py

Output
------
    _Data_Produced/results_fc_model.mat
    _Data_Produced/fc_model_corr.pdf
    _Data_Produced/fc_model_mse.pdf
"""

